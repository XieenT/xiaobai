//The diameter,circumference and area of a circle
#include <iostream>
using namespace std;
int main()
{
   double pi = 3.14159;
   int r;
   cin >> r;
   cout << 2 * r <<endl;
   cout << 2 * pi * r <<endl;
   cout << pi * r * r <<endl;
   return 0;
}
